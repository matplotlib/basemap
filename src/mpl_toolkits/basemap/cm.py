def _revcmap(data):
    """Return a reversed copy of a segment data dictionary."""

    data_r = {}
    for key, val in data.items():
        val = list(val)
        valrev = val[::-1]
        valnew = []
        for xval, color0, color1 in valrev:
            valnew.append((1.0 - xval, color0, color1))
        data_r[key] = valnew
    return data_r


